$(function() {
  $('.upload-image-wrapper').on('click', function() {
    $('body').find('#inputImage').trigger('click')
  })
  
  
   $('[data-cust-flag-modal]').on('click', function(e) {
    e.preventDefault()
    $('.cust-flag-privacy-modal').fadeIn({
        duration: 'slow',
        start: function() {
            $(this).css('display', 'flex')
        }
    })
  })

  $('.cust-flag-privacy-modal').click(function() {
    $(this).fadeOut()
  })

  $('body').on('click', 'span[data-cust-flag-privacy-modal-close]', function(e) {
    $('.cust-flag-privacy-modal').fadeOut()
  })

  $('.cust-flag-privacy-modal .inner-body').click(function(event) {
    if(!$(event.target).closest('span').length && !$(event.target).is('span')) {
      event.preventDefault();
      event.stopPropagation();
      event.stopImmediatePropagation();

      return false
    }
  })
})